    private void participantsListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_participantsListMouseClicked
        JList list = (JList) evt.getSource();
		if(evt.getClickCount() == 2) {
			int index = list.locationToIndex(evt.getPoint());
			if(index >= 0){
				Participant p = this.participantListModel.getParticipant(index);
				JOptionPane.showMessageDialog(rootPane, p, "Participant Info", JOptionPane.INFORMATION_MESSAGE);
			}
		}
    }//GEN-LAST:event_participantsListMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
		String selectedItem = (String) searchOptCmbBx.getSelectedItem();
		String query = queryTxBx.getText();
		List<Participant> found = new ArrayList<>();
		switch(selectedItem) {
			case "Name":
				found = this.olympicControlService.getParticipantByName(query);
				break;
			case "Swimming Style":
				found = this.olympicControlService.participantsBySwimStyle(query);
				break;
			case "University":
				found = this.olympicControlService.participantsByUniversity(query);
				break;
		}
		this.participantSearchListModel.setParticipants(found);
    }//GEN-LAST:event_jButton1ActionPerformed

