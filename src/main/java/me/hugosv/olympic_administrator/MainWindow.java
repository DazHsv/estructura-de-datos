    private void addPartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPartBtnActionPerformed
		Participant p = new Participant();
		p.setName(partNameTxBx.getText());
		p.setSwimStyle(partSwimStyleTxBx.getText());
		p.setSwimTime(Integer.parseInt(partTimeTxBx.getText()));
		p.setUniversity(partUnivTxBx.getText());
		if(this.olympicControlService.addParticipant(p)) {
			this.participantListModel.addParticipant(p);
			this.participants++;
			participantsNumber.setText(this.participants.toString());
			this.updateBestUniv();
			JOptionPane.showMessageDialog(rootPane, "Participant added!", "Success", JOptionPane.INFORMATION_MESSAGE);
		}
		
		partNameTxBx.setText("");
		partSwimStyleTxBx.setText("");
		partTimeTxBx.setText("");
		partUnivTxBx.setText("");
    }//GEN-LAST:event_addPartBtnActionPerformed

